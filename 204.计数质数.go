// @lc code=start
func countPrimes(n int) int {
	// isPrime := [n+1]bool
	isPrime := make([]bool, n+1)
	for i := 2; i <= n; i++ {
		isPrime[i] = true
	}
	for i := 2; i < n; i++ {
		if isPrime[i] {
			for j := i * i; j < n; j += i {
				isPrime[j] = false
			}
		}
	}
	counter := 0
	for i := 2; i < n; i++ {
		if isPrime[i] {
			counter++
		}
	}
	return counter
}

